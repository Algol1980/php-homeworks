//Задание 5. Найти сумму всех не четных чисел меньше переменной $a, если сумма меньше 500, вывести слово error, если более - success
echo '5. Найти сумму всех не четных чисел меньше переменной $a: ', $a, ' если сумма меньше 500, вывести слово error, если более - success: <br>';
for ($i = 1; $i < $a; $i += 2) {
    $sum += $i;
}
