//Задание 3. Есть три переменные $x, $y, $z, найти большую из них.
if ($x > $y && $x > $z) {
    $max = $x;
} elseif ($y > $x && $y > $z) {
    $max = $y;
} else {
    $max = $z;
}
echo '3. Есть три переменные ' . 'x: ' . $x . ' y: ' . $y . ' z: ' . $z . ' Максимальное: ', $max, '<br>';


