//Блок объявления переманных
$x = rand();
$y = rand();
$z = rand();
$a = rand(0, 100);
$sum = 0;
$sumThreeDigit = 0;
$numberThreeDigit = rand(100, 999);
$monthNumber = rand(1, 20);

//Задание 1. Найти произведение и деление $x и $y
echo '1. Найти произведение и деление $x: ' . $x  . ' и $y: ' . $y . ' Произведение: ' . $x * $y . ' Деление: ' . $x / $y . '<br>';


//Задание 2. Даны два числа. Найдите сумму их квадратов.
