$digits = (int)($numberThreeDigit - $hundreds * 100 - $dozens * 10);
$sumThreeDigit = $hundreds + $dozens + $digits;
echo '8. Дано трехзначное число ', $numberThreeDigit, '. Сумма его цифр: ', $sumThreeDigit, ' <br>';


